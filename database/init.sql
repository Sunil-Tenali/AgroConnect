CREATE OR REPLACE PROCEDURE place_order(
  IN p_customer_id VARCHAR
)
LANGUAGE plpgsql
AS $$
DECLARE
  v_customer_id VARCHAR(255) := LOWER(TRIM(p_customer_id));
  v_order_id INTEGER;
  v_address_id INTEGER;
  v_total_price NUMERIC(10,2);
  v_quantity INTEGER;
BEGIN
  SELECT address_id
  INTO v_address_id
  FROM contact_detail
  WHERE user_id = v_customer_id
  ORDER BY is_default DESC, address_id DESC
  LIMIT 1;

  IF v_address_id IS NULL THEN
    RAISE EXCEPTION 'No delivery address found for customer %', v_customer_id;
  END IF;

  SELECT
    COALESCE(SUM(sc.quantity), 0),
    COALESCE(SUM(p.price * sc.quantity), 0)
  INTO
    v_quantity,
    v_total_price
  FROM product_shoppingcart sc
  JOIN product p ON p.product_id = sc.product_id
  WHERE sc.customer_id = v_customer_id;

  IF v_quantity = 0 THEN
    RAISE EXCEPTION 'Cart is empty for customer %', v_customer_id;
  END IF;

  IF EXISTS (
    SELECT 1
    FROM product_shoppingcart sc
    JOIN product p ON p.product_id = sc.product_id
    WHERE sc.customer_id = v_customer_id
      AND (p.in_stock = FALSE OR p.available_units < sc.quantity)
  ) THEN
    RAISE EXCEPTION 'One or more products are out of stock or below requested quantity';
  END IF;

  INSERT INTO f2c_order (
    customer_id,
    total_price,
    order_date,
    shipping_price,
    delivery_address_id,
    delivery_date,
    order_status,
    quantity
  )
  VALUES (
    v_customer_id,
    v_total_price,
    CURRENT_DATE,
    50,
    v_address_id,
    CURRENT_DATE + 3,
    'Processing',
    v_quantity
  )
  RETURNING order_id INTO v_order_id;

  INSERT INTO order_product (order_id, product_id, quantity, price_at_order_time)
  SELECT
    v_order_id,
    sc.product_id,
    sc.quantity,
    p.price
  FROM product_shoppingcart sc
  JOIN product p ON p.product_id = sc.product_id
  WHERE sc.customer_id = v_customer_id;

  UPDATE product p
  SET available_units = p.available_units - sc.quantity
  FROM product_shoppingcart sc
  WHERE sc.product_id = p.product_id
    AND sc.customer_id = v_customer_id;

  INSERT INTO payment (order_id, customer_id, amount, payment_method, payment_status)
  VALUES (v_order_id, v_customer_id, v_total_price + 50, 'Cash On Delivery', 'Pending');

  DELETE FROM product_shoppingcart
  WHERE customer_id = v_customer_id;
END;
$$;

CREATE OR REPLACE PROCEDURE add_review(
  IN p_customer_id VARCHAR,
  IN p_product_id INTEGER,
  IN p_rating NUMERIC,
  IN p_review VARCHAR
)
LANGUAGE plpgsql
AS $$
BEGIN
  IF NOT EXISTS (
    SELECT 1
    FROM f2c_order o
    JOIN order_product op ON op.order_id = o.order_id
    WHERE o.customer_id = LOWER(TRIM(p_customer_id))
      AND op.product_id = p_product_id
  ) THEN
    RAISE EXCEPTION 'Customer can review only purchased products';
  END IF;

  INSERT INTO review (product_id, customer_id, rating, review, review_date)
  VALUES (p_product_id, LOWER(TRIM(p_customer_id)), p_rating, p_review, CURRENT_DATE)
  ON CONFLICT (customer_id, product_id)
  DO UPDATE SET
    rating = EXCLUDED.rating,
    review = EXCLUDED.review,
    review_date = CURRENT_DATE,
    created_at = CURRENT_TIMESTAMP;
END;
$$;

CREATE OR REPLACE PROCEDURE add_machinery(
  IN p_farmer_id VARCHAR,
  IN p_name VARCHAR,
  IN p_description VARCHAR,
  IN p_price_per_day NUMERIC,
  IN p_location VARCHAR
)
LANGUAGE plpgsql
AS $$
BEGIN
  INSERT INTO machinery (farmer_id, name, description, price_per_day, location, available)
  VALUES (LOWER(TRIM(p_farmer_id)), p_name, p_description, p_price_per_day, p_location, TRUE);
END;
$$;

CREATE OR REPLACE PROCEDURE rent_machinery(
  IN p_machinery_id INTEGER,
  IN p_renter_id VARCHAR,
  IN p_rental_date DATE DEFAULT CURRENT_DATE,
  IN p_return_date DATE DEFAULT NULL
)
LANGUAGE plpgsql
AS $$
BEGIN
  IF NOT EXISTS (
    SELECT 1 FROM machinery WHERE machinery_id = p_machinery_id AND available = TRUE
  ) THEN
    RAISE EXCEPTION 'Machinery % is not available', p_machinery_id;
  END IF;

  INSERT INTO machinery_rental (machinery_id, renter_id, rental_date, return_date, status)
  VALUES (p_machinery_id, LOWER(TRIM(p_renter_id)), p_rental_date, p_return_date, 'Active');

  UPDATE machinery
  SET available = FALSE
  WHERE machinery_id = p_machinery_id;
END;
$$;

CREATE OR REPLACE PROCEDURE return_machinery(
  IN p_rental_id INTEGER
)
LANGUAGE plpgsql
AS $$
DECLARE
  v_machinery_id INTEGER;
BEGIN
  SELECT machinery_id
  INTO v_machinery_id
  FROM machinery_rental
  WHERE rental_id = p_rental_id;

  IF v_machinery_id IS NULL THEN
    RAISE EXCEPTION 'Rental % not found', p_rental_id;
  END IF;

  UPDATE machinery_rental
  SET status = 'Returned', return_date = COALESCE(return_date, CURRENT_DATE)
  WHERE rental_id = p_rental_id;

  UPDATE machinery
  SET available = TRUE
  WHERE machinery_id = v_machinery_id;
END;
$$;

CREATE OR REPLACE PROCEDURE update_farmer_description(
  IN p_farmer_id VARCHAR,
  IN p_description VARCHAR
)
LANGUAGE plpgsql
AS $$
BEGIN
  UPDATE farmer
  SET description = p_description
  WHERE farmer_id = LOWER(TRIM(p_farmer_id));
END;
$$;

