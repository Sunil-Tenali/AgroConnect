CREATE OR REPLACE FUNCTION update_product_stock_status()
RETURNS TRIGGER
LANGUAGE plpgsql
AS $$
BEGIN
  NEW.in_stock := COALESCE(NEW.available_units, 0) > 0;
  NEW.updated_at := CURRENT_TIMESTAMP;
  RETURN NEW;
END;
$$;

CREATE TRIGGER trg_update_product_stock_status
BEFORE INSERT OR UPDATE OF available_units
ON product
FOR EACH ROW
EXECUTE FUNCTION update_product_stock_status();

CREATE OR REPLACE FUNCTION refresh_product_rating(p_product_id INTEGER)
RETURNS VOID
LANGUAGE plpgsql
AS $$
BEGIN
  UPDATE product
  SET
    rating = (
      SELECT ROUND(COALESCE(AVG(rating), 0)::numeric, 1)
      FROM review
      WHERE product_id = p_product_id
    ),
    review_count = (
      SELECT COUNT(*)
      FROM review
      WHERE product_id = p_product_id
    )
  WHERE product_id = p_product_id;
END;
$$;

CREATE OR REPLACE FUNCTION review_rating_trigger_fn()
RETURNS TRIGGER
LANGUAGE plpgsql
AS $$
DECLARE
  v_product_id INTEGER;
  v_farmer_id VARCHAR(255);
BEGIN
  IF TG_OP = 'DELETE' THEN
    v_product_id := OLD.product_id;
  ELSE
    v_product_id := NEW.product_id;
  END IF;

  PERFORM refresh_product_rating(v_product_id);

  SELECT farmer_id
  INTO v_farmer_id
  FROM product
  WHERE product_id = v_product_id;

  UPDATE farmer
  SET
    average_rating = (
      SELECT ROUND(COALESCE(AVG(rating), 0)::numeric, 1)
      FROM product
      WHERE farmer_id = v_farmer_id
    ),
    rating_count = (
      SELECT COALESCE(SUM(review_count), 0)
      FROM product
      WHERE farmer_id = v_farmer_id
    )
  WHERE farmer_id = v_farmer_id;

  RETURN COALESCE(NEW, OLD);
END;
$$;

CREATE TRIGGER trg_review_rating_update
AFTER INSERT OR UPDATE OR DELETE
ON review
FOR EACH ROW
EXECUTE FUNCTION review_rating_trigger_fn();

CREATE OR REPLACE FUNCTION calculate_average_farmer_rating(farmer_id_input VARCHAR)
RETURNS NUMERIC
LANGUAGE sql
AS $$
  SELECT ROUND(COALESCE(AVG(rating), 0)::numeric, 1)
  FROM product
  WHERE farmer_id = farmer_id_input;
$$;

CREATE OR REPLACE FUNCTION get_customer_cart(p_customer_id VARCHAR)
RETURNS TABLE (
  product_id INTEGER,
  product_name VARCHAR,
  farmer_id VARCHAR,
  farmer_name TEXT,
  price NUMERIC,
  quantity INTEGER,
  subtotal NUMERIC,
  available_units INTEGER,
  in_stock BOOLEAN
)
LANGUAGE sql
AS $$
  SELECT
    p.product_id,
    p.name AS product_name,
    p.farmer_id,
    CONCAT(u.fname, ' ', COALESCE(u.lname, '')) AS farmer_name,
    p.price,
    sc.quantity,
    p.price * sc.quantity AS subtotal,
    p.available_units,
    p.in_stock
  FROM product_shoppingcart sc
  JOIN product p ON p.product_id = sc.product_id
  JOIN f2c_user u ON u.email = p.farmer_id
  WHERE sc.customer_id = p_customer_id
  ORDER BY sc.added_at DESC;
$$;

CREATE OR REPLACE FUNCTION get_customer_orders(p_customer_id VARCHAR)
RETURNS TABLE (
  order_id INTEGER,
  product_id INTEGER,
  product_name VARCHAR,
  quantity INTEGER,
  price NUMERIC,
  order_date DATE,
  delivery_date DATE,
  status VARCHAR,
  total_price NUMERIC
)
LANGUAGE sql
AS $$
  SELECT
    o.order_id,
    p.product_id,
    p.name AS product_name,
    op.quantity,
    op.price_at_order_time AS price,
    o.order_date,
    o.delivery_date,
    o.order_status AS status,
    o.total_price
  FROM f2c_order o
  JOIN order_product op ON op.order_id = o.order_id
  JOIN product p ON p.product_id = op.product_id
  WHERE o.customer_id = p_customer_id
  ORDER BY o.order_date DESC, o.order_id DESC;
$$;

CREATE OR REPLACE PROCEDURE register_customer(
