CREATE OR REPLACE PROCEDURE add_product(
  IN p_name VARCHAR,
  IN p_farmer_id VARCHAR,
  IN p_price NUMERIC,
  IN p_category_id INTEGER,
  IN p_description VARCHAR,
  IN p_available_units INTEGER,
  IN p_carrier_phone VARCHAR
)
LANGUAGE plpgsql
AS $$
DECLARE
  v_carrier_id INTEGER;
BEGIN
  SELECT carrier_id
  INTO v_carrier_id
  FROM carrier
  WHERE carrier_phone = p_carrier_phone
  LIMIT 1;

  IF v_carrier_id IS NULL THEN
    RAISE EXCEPTION 'No carrier found with phone number %', p_carrier_phone;
  END IF;

  INSERT INTO product (
    name,
    farmer_id,
    price,
    category_id,
    description,
    available_units,
    carrier_id
  )
  VALUES (
    p_name,
    LOWER(TRIM(p_farmer_id)),
    p_price,
    p_category_id,
    p_description,
    p_available_units,
    v_carrier_id
  );
END;
$$;

CREATE OR REPLACE PROCEDURE add_to_shopping_cart(
  IN p_customer_id VARCHAR,
  IN p_product_id INTEGER
)
LANGUAGE plpgsql
AS $$
BEGIN
  INSERT INTO shopping_cart (customer_id)
  VALUES (LOWER(TRIM(p_customer_id)))
  ON CONFLICT (customer_id) DO NOTHING;

  IF NOT EXISTS (
    SELECT 1
    FROM product
    WHERE product_id = p_product_id
      AND in_stock = TRUE
      AND available_units > 0
  ) THEN
    RAISE EXCEPTION 'Product % is not available', p_product_id;
  END IF;

  INSERT INTO product_shoppingcart (customer_id, product_id, quantity)
  VALUES (LOWER(TRIM(p_customer_id)), p_product_id, 1)
  ON CONFLICT (customer_id, product_id)
  DO UPDATE SET
    quantity = product_shoppingcart.quantity + 1,
    added_at = CURRENT_TIMESTAMP;
END;
$$;

CREATE OR REPLACE PROCEDURE update_cart_quantity(
  IN p_customer_id VARCHAR,
  IN p_product_id INTEGER,
  IN p_quantity INTEGER
)
LANGUAGE plpgsql
AS $$
DECLARE
  v_available_units INTEGER;
BEGIN
  IF p_quantity <= 0 THEN
    DELETE FROM product_shoppingcart
    WHERE customer_id = LOWER(TRIM(p_customer_id))
      AND product_id = p_product_id;
    RETURN;
  END IF;

  SELECT available_units
  INTO v_available_units
  FROM product
  WHERE product_id = p_product_id;

  IF v_available_units IS NULL THEN
    RAISE EXCEPTION 'Product % not found', p_product_id;
  END IF;

  IF p_quantity > v_available_units THEN
    RAISE EXCEPTION 'Only % units available for product %', v_available_units, p_product_id;
  END IF;

  UPDATE product_shoppingcart
  SET quantity = p_quantity
  WHERE customer_id = LOWER(TRIM(p_customer_id))
    AND product_id = p_product_id;
END;
$$;

CREATE OR REPLACE PROCEDURE remove_from_shopping_cart(
